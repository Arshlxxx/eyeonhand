import numpy as np

# 定义 4x4 矩阵 A
A = np.array([
    [0.98406, 2.4397e-05, 0.17784, 0.30051],
    [-4.183e-06, 1, -0.00011404, -0.00028522],
    [-0.17784, 0.00011148, 0.98406, 0.37234],
    [0, 0, 0, 1]
])

# 定义 4x1 向量 v
v = np.array([0.33934, -0.029119, -0.12895, 1])

# 计算矩阵 A 和向量 v 的乘积
result = np.dot(A, v)

# 输出结果
print("结果为: ", result)

